package com.sos.ui5.battle.sos.simulator;

public class RoundDetail {
	int dead = 0;
	double damage = 0;

	public int getDead() {
		return dead;
	}

	public void setDead(int dead) {
		this.dead = dead;
	}

	public double getDamage() {
		return damage;
	}

	public void setDamage(double damage) {
		this.damage = damage;
	}

}
